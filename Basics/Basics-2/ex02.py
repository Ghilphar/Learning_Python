num_first = int(input("Give me a first number.\n"))
num_second = int(input("Give me a second number.\n"))

sum = num_first + num_second
difference = num_first - num_second
product = num_first * num_second
quotient = num_first / num_second

print("The sum is " + str(sum))
print("The difference is " + str(difference))
print("The product is " + str(product))
print("The quotient is " + str(quotient))
